#ifndef _idwg_h_
#define _idwg_h_

#include "headfile.h"
#include "bldc_config.h"


void        system_delay            (uint32 time, uint32 num);

//====================================================宏定义函数区====================================================
//-------------------------------------------------------------------------------------------------------------------
// 函数简介     system 延时函数 ms 级别
// 参数说明     time            需要延时的时间 ms 级别
// 返回参数     void
// 使用示例     system_delay_ms(100);
// 备注信息
//-------------------------------------------------------------------------------------------------------------------
#define     system_delay_ms(time)   system_delay((SystemCoreClock/8000), (time))             // 设置延时时间 单位 ms

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     system 延时函数 us 级别
// 参数说明     time            需要延时的时间 us 级别
// 返回参数     void
// 使用示例     system_delay_us(100);
// 备注信息     受限于程序运行跳转 此延时会比输入值高出一些
//-------------------------------------------------------------------------------------------------------------------
#define     system_delay_us(time)   system_delay((SystemCoreClock/8000000), time)            // 设置延时时间 单位 us 范围0 - 16777215(0xffffff)/(DELAY_CLK*1000)ms  主频 120M systick 最大延时时间大约为 139ms

//====================================================宏定义函数区====================================================

#endif
