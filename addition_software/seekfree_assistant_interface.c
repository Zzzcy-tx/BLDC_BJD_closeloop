#include "seekfree_assistant.h"
#include "usart.h"
//-------------------------------------------------------------------------------------------------------------------
// 函数简介     逐飞助手发送函数
// 参数说明     *buff           需要发送的数据地址
// 参数说明     length          需要发送的长度
// 返回参数     uint32          剩余未发送数据长度
// 使用示例
//-------------------------------------------------------------------------------------------------------------------
__attribute__((weak)) uint32 seekfree_assistant_transfer (const uint8 *buff, uint32 length)
{
    uart_write_buffer(buff, length);
    return 0;
}



